export class Voiture {
    public type:any;
    public vehicule:any;
    public prix:any;
    public carburant:any;
    public puissance:any;
    public supplementperday:any;
    constructor(type,vehicule,prix,carburant,puissance,supplementperday){
        this.type=type;
        this.vehicule=vehicule;
        this.prix=prix;
        this.carburant=carburant;
        this.puissance=puissance;
        this.supplementperday=supplementperday;
    }
}
